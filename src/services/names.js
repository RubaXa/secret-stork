let _names = []

export async function loadNames() {
  L('names#load', 'start')
  try {
    const db = await getDB()
    const cached = await db.getAll('names')
    if (cached.length > 0) {
      _names = cached
      L('names#load', 'IDB cache hit count=' + cached.length)
      return _names
    }
  } catch (_) {}

  try {
    const r = await fetch(import.meta.env.BASE_URL + 'data/names_enriched.json')
    if (r.ok) {
      const enriched = await r.json()
      _names = enriched
      const db = await getDB()
      const tx = db.transaction('names', 'readwrite')
      await Promise.all([...enriched.map(n => tx.store.put(n)), tx.done])
      L('names#load', 'fetched and cached count=' + enriched.length)
      return _names
    }
  } catch (e) {
    L('names#load', 'fetch error', e.message)
  }

  _names = ALL_NAMES.map(n => ({ name: n }))
  L('names#load', 'fallback to ALL_NAMES count=' + _names.length)
  return _names
}

export function getNames() {
  return _names
}

export function getNamesByGroups(keys, names) {
  const list = names || _names
  if (!keys || keys.length === 0 || keys.includes('all')) return list
  const originSet = new Set(keys.flatMap(k => (ORIGIN_GROUPS.find(g => g.key === k) || {}).origins || []))
  return list.filter(n => originSet.has(n.origin))
}
